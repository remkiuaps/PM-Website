let currentLang = localStorage.getItem('pm-lang') || 'pl';

function t(key) {
  return translations[currentLang]?.[key] || translations['pl']?.[key] || key;
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('pm-lang', lang);
  translatePage();
  updateLangButtons();
}

function translatePage() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const value = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = value;
    } else {
      el.innerHTML = value;
    }
  });
  document.documentElement.lang = currentLang;
}

function updateLangButtons() {
  document.querySelectorAll('.lang-switcher__track').forEach(track => {
    track.setAttribute('data-active', currentLang);
  });
  document.querySelectorAll('.lang-switcher__option').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  translatePage();
  updateLangButtons();

  document.querySelectorAll('.lang-switcher__option').forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
  });
});
